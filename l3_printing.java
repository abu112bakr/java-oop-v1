public class l3_printing {
    public static void main(String[] args) {
        System.out.println("we dont need an object or instance to call a static method, this is executed automatically");
        System.out.println("System.out.println() - prints a line");
        System.out.println("System.out.print() - prints without a newline");
        System.out.printf("System.out.printf() - formatted printing");
    }
}