public class l12_java_loops1 {
    public static void main (String [] args){
        // while loop need initialization
        int i = 0;
        while (i<50) {
            if (i%2==0){
                System.out.println("CSE IS FUN Even Count="+i);
            } else{
                System.out.println("CSE IS FUN Odd Count="+i);
            }
            i++;
        }




    }
}
